<h3>Petunjuk Penggunaan Sistem Data Jemaat</h3>
Sebelum anda menggunakan sistem ini anda terlebih dahulu harus mengerti dan memahami cara kerja dari sistem ini, sehingga nantinya anda tidak akan kesulitan dalam menginputkan data Jemaat, data yang dihasilkanpun akan bagus.<br><br>

Berikut adalah Petunjuk Penggunaannya :<br>
1. Anda silahkan dengan login pada menu yang tersedi dengan username dan password masing-masing<br>
2. Yang pertama kali adalah menyiapkan instrumen yang sudah terisi dengan data detail anggota<br>
3. Masukkan data anggota secara runtut dari data diri ayah-ibu dan anak-anak sampai data penyerahan anak-anak<br>
4. Pengisian sesuai dengan form yang tersedia, ada beberapa form yang memang tidak boleh kosong ( harus diisi ).<br>
5. Pastikan semua data telah masuk dalam sistem dengan benar<br>

